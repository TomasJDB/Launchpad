<p><b><a name="q1" id="q1"> </a>Q: What exactly is a
        payday loan?<br />
    </b><b>A:</b> A payday loan is a small unsecured
    loan, usually for $100 to $1,000, which is designed to meet your financial
    needs until your next payday. Essentially, you are borrowing funds for the
    advanced loan amount, plus a fee, to be held until your next payday.</p>
<p>Payday
    loans are also known as cash advance, short term loans, payday advances, cash
    loans, fast cash, fast loans, bad credit loans, deferred deposit, and a number
    of other names. No matter what you call them, payday loans can get you through
    your financial crisis until your next scheduled payday.</p>
